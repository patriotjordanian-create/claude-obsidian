#!/usr/bin/env bash
# Weekly review (mechanical half).
# Summarizes the past 7 days of vault activity from git history into
# wiki/meta/weekly-review-YYYY-MM-DD.md. The reflective half (what it means,
# what to do next week) belongs to a Claude session reading this report.
# Usage: bash bin/agents/weekly-review.sh   (cron-safe, no arguments)

set -euo pipefail
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
VAULT="$(dirname "$(dirname "$SCRIPT_DIR")")"
cd "$VAULT"

TODAY=$(date -u +%F)
OUT="wiki/meta/weekly-review-$TODAY.md"

COMMITS=$(git log --since="7 days ago" --oneline -- wiki .raw | wc -l | tr -d ' ')
CREATED=$(git log --since="7 days ago" --diff-filter=A --name-only --pretty=format: -- wiki | grep -c '\.md$' || true)
UPDATED=$(git log --since="7 days ago" --diff-filter=M --name-only --pretty=format: -- wiki | grep '\.md$' | sort -u | wc -l | tr -d ' ')

{
  echo "---"
  echo "type: meta"
  echo "title: \"Weekly Review $TODAY\""
  echo "created: $TODAY"
  echo "updated: $TODAY"
  echo "tags: [meta, weekly-review]"
  echo "status: developing"
  echo "---"
  echo
  echo "# Weekly Review: $TODAY"
  echo
  echo "Generated by \`bin/agents/weekly-review.sh\` from git history (last 7 days)."
  echo
  echo "## Activity"
  echo
  echo "- Wiki commits: $COMMITS"
  echo "- Pages created: $CREATED"
  echo "- Pages updated: $UPDATED"
  echo
  echo "## Commits"
  echo
  git log --since="7 days ago" --pretty=format:'- %ad %s' --date=short -- wiki .raw
  echo
  echo
  echo "## Next step"
  echo
  echo "Ask Claude: \"review this week\" to turn this raw activity into insights."
} > "$OUT"

echo "weekly review written: $OUT"
